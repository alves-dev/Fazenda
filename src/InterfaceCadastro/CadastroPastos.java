    private void botaoCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoCalcularActionPerformed

        int A, B, C, D, diagonal;
        A = Integer.parseInt(campoA.getText());
        B = Integer.parseInt(campoB.getText());
        C = Integer.parseInt(campoC.getText());
        D = Integer.parseInt(campoD.getText());
        diagonal = Integer.parseInt(campoDiagonal.getText());

        double perimetro1, perimetro2;

        perimetro1 = A + B + diagonal;
        perimetro2 = C + D + diagonal;

        //calculo perimetro1
        perimetro1 = perimetro1 / 2;

        double rais1 = perimetro1 * (perimetro1 - A) * (perimetro1 - B) * (perimetro1 - diagonal);
        rais1 = Math.sqrt(rais1);

        //calculo perimetro2
        perimetro2 = perimetro2 / 2;

        double rais2 = perimetro2 * (perimetro2 - C) * (perimetro2 - D) * (perimetro2 - diagonal);
        rais2 = Math.sqrt(rais2);

        //totais
        int area = (int) (rais1 + rais2);

        String areaS = String.valueOf(area);
        String perimetroTotal = String.valueOf(A + B + C + D);
        campoArea.setText(areaS);
        campoPerimetro.setText(perimetroTotal);
    }//GEN-LAST:event_botaoCalcularActionPerformed

