    public void consultaPastos() throws SQLException {

        conexao conexao = new conexao();
        conexao.getConexao();
        String selectSQL = "SELECT * FROM pastos A where a.cod_fazenda = " + cod_fazenda;

        Statement pstmtCon;
        pstmtCon = conexao.getConexao().createStatement();

        ResultSet rs = pstmtCon.executeQuery(selectSQL);

        while (rs.next()) {

            DefaultTableModel tabela = (DefaultTableModel) tabelaResultado.getModel();
            tabela.addRow(new String[]{rs.getString("cod_pasto"), rs.getString("pasto"), rs.getString("status"),
                rs.getString("ladoA"), rs.getString("ladoB"), rs.getString("ladoC"), rs.getString("ladoD"),
                rs.getString("diagonal"), rs.getString("perimetro"), rs.getString("area"), rs.getString("dtCadastro"), rs.getString("dtUltimaAteracao")});

        }

        rs.close();
        pstmtCon.close();

    }

    public void codSelecao() throws SQLException {

        conexao conexao = new conexao();
        conexao.getConexao();
        String selectSQL = "SELECT * FROM statusPastos A WHERE A.status LIKE '" + selecao + "'";

        Statement pstmtCon;
        pstmtCon = conexao.getConexao().createStatement();

        ResultSet rs = pstmtCon.executeQuery(selectSQL);

        while (rs.next()) {

            codSelecao = Integer.parseInt(rs.getString("cod_status"));
        }

        rs.close();
        pstmtCon.close();

    }
