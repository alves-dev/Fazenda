    private void somaTotais() {
        int totalAnimais = tabelaVendaAnimais.getRowCount();
        int mortos = 0;
        if (!jtAnimaisMortos.getText().equals("")) {
            mortos = Integer.parseInt(jtAnimaisMortos.getText());
        }

        int macho = 0, femea = 0, linha = 0;
        if (totalAnimais > 0) {
            while (linha < totalAnimais) {
                String sexo = (String) tabelaVendaAnimais.getValueAt(linha, 4);
                if (sexo.equals("M")) {
                    macho++;
                }
                if (sexo.equals("F")) {
                    femea++;
                }
                linha++;
            }
            jtAnimaisFemeas.setText(String.valueOf(femea));
            jtAnimaisMachos.setText(String.valueOf(macho));

            if (!jTTotalArroba.getText().equals("")) {
                int totalArroba = Integer.parseInt(jTTotalArroba.getText());
                double mediaArroba = totalArroba / totalAnimais;
                jTMediaArroba.setText(String.valueOf(mediaArroba));
            }

        }else{
            jtAnimaisFemeas.setText("0");
            jtAnimaisMachos.setText("0");
            jtTotalAnimais.setText("0");
        }

        totalAnimais = totalAnimais + mortos;
        jtTotalAnimais.setText(String.valueOf(totalAnimais));

        if (!jTReaisArroba.getText().equals("") && !jTTotalArroba.getText().equals("")) {
            int reaisArroba = Integer.parseInt(jTReaisArroba.getText());
            int totalArroba = Integer.parseInt(jTTotalArroba.getText());

            int manejo = 0;
            if (!jTReaisManejo.getText().equals("")) {
                manejo = Integer.parseInt(jTReaisManejo.getText());
            }

            double totalReias = manejo + (reaisArroba * totalArroba);
            jTReaisTotal.setText(String.valueOf(totalReias));
        }

    }

