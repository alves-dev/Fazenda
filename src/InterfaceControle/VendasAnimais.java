    private void compoTipoVendaPopupMenuWillBecomeInvisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_compoTipoVendaPopupMenuWillBecomeInvisible
        int index = compoTipoVenda.getSelectedIndex();
        /*
        1 == pesquisa por lote
        2 == pesquisa por pasto
        3 == pesquisa por animais
         */

        if (index == 1) {
            pesquisa_Id.setEnabled(false);
            pesquisa_SegundaId.setEnabled(false);
            pesquisa_Idade.setEnabled(false);
            pesquisa_comboPasto.setEnabled(false);
            pesquisa_ComboTipoAnimal.setEnabled(false);
            pesquisa_Sexo.setEnabled(false);
            pesquisa_lote.setEnabled(false);
            BotaoPesquisa_Pesquisa.setEnabled(false);

            comboPasto.setEnabled(false);

            comboLote.setEnabled(true);

            try {
                pesquisaLote();
            } catch (SQLException ex) {
                Logger.getLogger(VendasAnimais.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if (index == 2) {
            pesquisa_Id.setEnabled(false);
            pesquisa_SegundaId.setEnabled(false);
            pesquisa_Idade.setEnabled(false);
            pesquisa_comboPasto.setEnabled(false);
            pesquisa_ComboTipoAnimal.setEnabled(false);
            pesquisa_Sexo.setEnabled(false);
            pesquisa_lote.setEnabled(false);
            BotaoPesquisa_Pesquisa.setEnabled(false);

            comboLote.setEnabled(false);

            comboPasto.setEnabled(true);

            try {
                pesquisaPasto();
            } catch (SQLException ex) {
                Logger.getLogger(VendasAnimais.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        if (index == 3) {
            comboPasto.setEnabled(false);

            comboLote.setEnabled(false);

            pesquisa_Id.setEnabled(true);
            pesquisa_SegundaId.setEnabled(true);
            pesquisa_Idade.setEnabled(true);
            pesquisa_comboPasto.setEnabled(true);
            pesquisa_ComboTipoAnimal.setEnabled(true);
            pesquisa_Sexo.setEnabled(true);
            pesquisa_lote.setEnabled(true);
            BotaoPesquisa_Pesquisa.setEnabled(true);
            tabelaPesquisaAnimais.setEnabled(true);
            try {
                pesquisaTipoAnimal();
                pesquisaPasto();
            } catch (SQLException ex) {
                Logger.getLogger(VendasAnimais.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_compoTipoVendaPopupMenuWillBecomeInvisible

