    private void pesquisaAnimais(Integer id, Integer idade, Integer lote, String pasto,
            String sexo, String tipoAnimal, String segundaId) throws SQLException {

        conexao conexao = new conexao();
        conexao.getConexao();
        String selectSQL = "SELECT * FROM animais A where A.cod_status <> 1 and a.cod_status <> 3 and a.cod_fazenda = " + cod_fazenda;

        if (id != null) {
            selectSQL = selectSQL + " and A.identificacao = " + id;
        }
        if (idade != null) {
            selectSQL = selectSQL + " and A.idade = " + idade;
        }
        if (lote != null) {
            selectSQL = selectSQL + " and A.loteCompra = " + lote;
        }
        if (!pasto.equals("Selecionar")) {
            selectSQL = selectSQL + " and A.pasto like '" + pasto + " ' ";
        }
        if (!sexo.equals("")) {
            selectSQL = selectSQL + " and A.sexo = '" + sexo + " ' ";
        }
        if (!tipoAnimal.equals("Selecionar")) {
            selectSQL = selectSQL + " and A.tipoAnimal like '" + tipoAnimal + " ' ";
        }
        if (!segundaId.equals("")) {
            selectSQL = selectSQL + " and A.identificacao2 like '" + segundaId + " ' ";
        }

        Statement pstmtCon;
        pstmtCon = conexao.getConexao().createStatement();

        ResultSet rs = pstmtCon.executeQuery(selectSQL);

        while (rs.next()) {

            DefaultTableModel tabela = (DefaultTableModel) tabelaPesquisaAnimais.getModel();
            tabela.addRow(new String[]{rs.getString("identificacao"), rs.getString("loteCompra"),
                rs.getString("pasto"), rs.getString("idade"),
                rs.getString("Sexo"), rs.getString("tipoAnimal")});

        }

        tabelaPesquisaAnimais.setAutoResizeMode(tabelaPesquisaAnimais.AUTO_RESIZE_OFF); //adiciona barra de rolagem horizontal na tabela
        tabelaPesquisaAnimais.setDefaultRenderer(Object.class, new CellRenderer()); //chama class CellRenderer para centralizar conteudo da tabela

        rs.close();
        pstmtCon.close();
    }

    private void limparTabela(JTable nomeTabela) {
        DefaultTableModel tabela = (DefaultTableModel) nomeTabela.getModel();
        int numeroLinhas = tabela.getRowCount();
        int index = 1;

        while (index <= numeroLinhas) {
            tabela.removeRow(0);
            index++;
        }

        somaTotais();
    }

