    private void BotaoPesquisa_PesquisaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotaoPesquisa_PesquisaActionPerformed
        Integer id = null, idade = null, lote = null;

        if (!pesquisa_Id.getText().equals("")) {
            id = Integer.parseInt(pesquisa_Id.getText());
        }
        if (!pesquisa_Idade.getText().equals("")) {
            idade = Integer.parseInt(pesquisa_Idade.getText());
        }
        if (!pesquisa_lote.getText().equals("")) {
            lote = Integer.parseInt(pesquisa_lote.getText());
        }

        String pasto = (String) pesquisa_comboPasto.getSelectedItem();
        String sexo = pesquisa_Sexo.getText();
        String tipoAnimal = (String) pesquisa_ComboTipoAnimal.getSelectedItem();
        String segundaId = pesquisa_SegundaId.getText();

        limparTabela(tabelaPesquisaAnimais);

        try {
            pesquisaAnimais(id, idade, lote, pasto, sexo, tipoAnimal, segundaId);
        } catch (SQLException ex) {
            Logger.getLogger(VendasAnimais.class.getName()).log(Level.SEVERE, null, ex);
        }
        somaTotais();
    }//GEN-LAST:event_BotaoPesquisa_PesquisaActionPerformed

